package myquoter_gui;

public class BodyDoubleWeft {
	
		double bodyD;
		
		public double bodyDoubleWeft(int index) {
			
			switch(index) {
				case 0: bodyD = 47; break;
				case 1: bodyD = 49.50; break;
				case 2: bodyD = 53; break;
				case 3: bodyD = 55.50; break;
				case 4: bodyD = 58; break;
				case 5: bodyD = 60.50; break;
				case 6: bodyD = 63; break;
				case 7: bodyD = 65.50; break;
				case 8: bodyD = 68; break;
				case 9: bodyD = 70.50; break;
				case 10: bodyD = 73; break;
			}
			return bodyD;
		}
}
		   