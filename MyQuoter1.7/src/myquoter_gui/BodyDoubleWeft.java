package myquoter_gui;

public class BodyDoubleWeft {
	
		double bodyD;
		
		public double bodyDoubleWeft(int index) {
			
			switch(index) {
				case 0: bodyD = 49; break;
				case 1: bodyD = 51.50; break;
				case 2: bodyD = 55; break;
				case 3: bodyD = 57.50; break;
				case 4: bodyD = 60; break;
				case 5: bodyD = 62.50; break;
				case 6: bodyD = 65; break;
				case 7: bodyD = 67.50; break;
				case 8: bodyD = 70; break;
				case 9: bodyD = 72.50; break;
				case 10: bodyD = 75; break;
			}
			return bodyD;
		}
}
		   